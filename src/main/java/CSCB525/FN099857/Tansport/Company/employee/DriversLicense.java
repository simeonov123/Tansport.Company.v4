/**
 * Enumeration representing different types of driver's licenses.
 * Each value corresponds to a specific category of driver's license.
 * The driver's license categories are defined as follows:
 * - AM: Mopeds
 * - A1: Light motorcycle
 * - A2: Standard motorcycle
 * - A: Unrestricted motorcycle
 * - B1: Three-wheeled vehicles (trikes)
 * - B: Cars and light trucks
 * - BE: Cars and light trucks with trailer
 * - C1: Medium-sized vehicles
 * - C1E: Medium-sized vehicles with trailer
 * - C: Large vehicles
 * - CE: Large vehicles with trailer
 * - D1: Minibuses
 * - D1E: Minibuses with trailer
 * - D: Buses
 * - DE: Buses with trailer
 * - TKT: Tractors (short term)
 * - TTM: Tractors (long term)
 *
 */
public enum DriversLicense {
    AM, A1, A2, A, B1, B, BE, C1, C1E, C, CE, D1, D1E, D, DE, TKT, TTM
}